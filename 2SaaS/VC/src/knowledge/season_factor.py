def get_season_factor(cuisine: str, month: int) -> float:
    """업종-월 시즌팩터 조회. 없으면 1.0.

    매칭 전략:
      1. cuisine 정확 일치
      2. cuisine 부분 일치 (등록 키가 cuisine에 포함되거나 그 반대)
         예: cuisine="돈까스 전문점" → "돈까스·회·일식" 키의 "돈까스" 토큰 매칭
      3. 어느 쪽도 실패 시 1.0

    Args:
        cuisine: 업종명 (예: "치킨", "중식", "돈까스·회·일식")
        month: 1~12 (1-indexed)

    Returns:
        시즌팩터 (보통 0.8~1.6 범위)

    Raises:
        ValueError: month가 1~12 범위 밖
    """
    if not (1 <= month <= 12):
        raise ValueError(f"month는 1~12 (1-indexed). 입력: {month}")

    if not cuisine:
        return 1.0

    # 1. 정확 일치
    if cuisine in SEASON_FACTORS:
        return SEASON_FACTORS[cuisine][month - 1]

    # 2. 부분 일치 (조합 키 하위 토큰)
    for key, factors in SEASON_FACTORS.items():
        # 키 자체가 cuisine 에 포함 (예: "치킨" in "안성치킨")
        if key in cuisine:
            return factors[month - 1]
        # 조합 키("·" 또는 "/") 하위 토큰 중 하나가 cuisine 에 포함
        parts = [p.strip() for p in key.replace("·", "/").split("/")]
        for p in parts:
            if p and p in cuisine:
                return factors[month - 1]

    # 3. 미등록 업종
    return 1.0
